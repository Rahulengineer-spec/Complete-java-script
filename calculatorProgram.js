function  calculateSum(a,b){
    sum=a+b;
    return sum;
}
function calculateSub(a,b){
    sub=a-b;
    return sub;
}
function calculateMul(a,b){
    mul=a*b;
    return mul;
}
function calculateDiv(a,b){
    div=a/b;
    return div;
}
function calculateMod(a,b){
    mod=a%b;
    return mod;
}

console.log(calculateSum(10,20));
console.log(calculateMul(13,99));
console.log(calculateDiv(23456,56));

